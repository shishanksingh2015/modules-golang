package main

import (
	"fmt"
	"sort"
)

func reverse(sw []int) []int {
	for a, b := 0, len(sw)-1; a < b; a, b = a+1, b-1 {
		sw[a], sw[b] = sw[b], sw[a]
	}
	return sw
}

func sortString(s string) string {
	runes := []rune(s)
	sort.Slice(runes, func(i, j int) bool {
		return runes[i] < runes[j]
	})
	return string(runes)
}

func main() {
	fmt.Println(reverse([]int{1, 2, 3, 4}))
}
